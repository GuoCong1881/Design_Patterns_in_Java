package BehavioralPatterns.Exercise_TemplateMethod;

public class Main_TemplateMethod {
    /**
     * Template Method Pattern
     *
     * - You define a base class or interface for a procedure or algorithm, but leave empty placeholders for
     * some parts of the procedure.
     * - Each placeholder is a blank or default method in the base class.
     * - This base class acts as a template.
     * - Callers fill in the blanks by extending the base class and overriding the placeholder methods.
     */
}
