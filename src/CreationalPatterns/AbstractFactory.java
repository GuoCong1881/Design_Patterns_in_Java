package CreationalPatterns;

public class AbstractFactory {
    /**
     * Factory:
     *
     * is anything that creates objects. Factories are useful for hiding construction details from callers.
     *
     * - If the thing creating objects is a method, it's known as a factory method. You've already seen some examples
     * of factory methods, such as Stream.of().
     * - If the thing creating objects is also an object, it's known as an abstract factory. These are useful
     * when you want to separate construction of objects into a completely separate Java interface.
     *
     * When to Use Abstract Factories?
     *
     * You want to hide construction details from callers.
     * You want to encapsulate construction of several related objects into a single Java interface.
     */
}
